function createTransactionDomain({
  createTransactionService,
  createTransactionMutationService,
  db,
  userHasPermission,
  makeDocNo,
  addSupplierLedgerEntry,
  addCustomerLedgerEntry,
  addTreasuryTransaction,
  addAuditLog,
  persistAppStateOnly,
  hydrateRelationalCollections,
  getStoredAppState,
  relationalPurchases,
  relationalSales,
  resolveBranchLocationScope,
  relationalCustomers,
  relationalProducts,
  relationalSuppliers,
  relationalReturns,
  assertManagerPin,
  assertSaleMutationAllowed,
  assertPurchaseMutationAllowed,
  normalizeIncomingSale,
  normalizeIncomingPurchase,
}) {
  const transactionService = createTransactionService({
    db,
    userHasPermission,
    makeDocNo,
    addSupplierLedgerEntry,
    addCustomerLedgerEntry,
    addTreasuryTransaction,
    addAuditLog,
    persistAppStateOnly,
    hydrateRelationalCollections,
    getStoredAppState,
    relationalPurchases,
    relationalSales,
    resolveBranchLocationScope,
    relationalCustomers,
    relationalProducts,
    relationalSuppliers,
    relationalReturns,
    assertManagerPin,
  });

  const transactionMutationBase = createTransactionMutationService({
    db,
    assertManagerPin,
    assertSaleMutationAllowed,
    assertPurchaseMutationAllowed,
    normalizeIncomingSale,
    normalizeIncomingPurchase,
    userHasPermission,
    addCustomerLedgerEntry,
    addSupplierLedgerEntry,
    addTreasuryTransaction,
    addAuditLog,
    persistAppStateOnly,
    hydrateRelationalCollections,
    getStoredAppState,
    relationalSales,
    relationalPurchases,
  });

  return {
    transactionService,
    transactionMutationService: {
      cancelSaleRecord: transactionMutationBase.cancelSaleRecord,
      cancelPurchaseRecord: transactionMutationBase.cancelPurchaseRecord,
      updateSaleRecord: transactionMutationBase.updateSaleRecord,
      updatePurchaseRecord: transactionMutationBase.updatePurchaseRecord,
    },
  };
}

module.exports = { createTransactionDomain };
